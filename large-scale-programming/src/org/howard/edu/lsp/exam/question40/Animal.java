package org.howard.edu.lsp.exam.question40;

/**
 * The abstract Animal class.
 */
public abstract class Animal {
    /**
     * The method that outputs when an Animal instance speaks.
     */
    public void speak() {
        System.out.println("The animal speaks");
    }
    /**
     * The method that outputs when an Animal instance move.
     */
    public void move() {
        System.out.println("The animal moves forward");
    }
}

